def generate_reasoning_dataset(output_path: str, num_samples: int = 100):
    """生成推理数据集"""
    samples = []
    
    for i in range(num_samples):
        base_sample = random.choice(REASONING_SAMPLES)
        
        formatted_response = f"{THINK_START}\n{base_sample['thinking']}\n{THINK_END}\n\n{ANSWER_START}\n{base_sample['answer']}\n{ANSWER_END}"
        
        sample = {
            "conversations": [
                {"role": "user", "content": base_sample["question"]},
                {"role": "assistant", "content": formatted_response}
            ]
        }
        samples.append(sample)
    
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        for sample in samples:
            f.write(json.dumps(sample, ensure_ascii=False) + '\n')
    
    print(f"已生成 {len(samples)} 条推理训练数据到 {output_path}")
    return samples

if __name__ == "__main__":
    generate_reasoning_dataset("data/processed/reasoning_train.jsonl", num_samples=100)
